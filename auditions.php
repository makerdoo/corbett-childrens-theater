          <h3 id="h1-cent" class="underline">Things Not to Do at an Audition</h3>
          <br>
          <p>Auditioning is hard.  Putting yourself on the line for the chance at a role can be scary. Walking out onto a stage alone in front of a dark room or a panel of directors can be unnerving. Part of that is the fact that you just don't know what to expect and / or what the director is looking for.  That's just the way it is, but you can help yourself by memorizing and avoiding these common auditioning traps.</p>

          <ul>
            <li><strong>Do Not… approach auditions for musicals like you would for a spot on The Voice:</strong>  Musicals are about regular people living their lives and the songs are either their thoughts or conversations.  While it can sometimes be beautiful, first and foremost it is about bringing forth a character that moves the story forward. Be real.  Don’t show us your inner opera singer. Instead, show us a person who is struggling or celebrating with the subject matter of the song.
            </li> 
            &nbsp;
            <li><strong>Do Not… choose a song or monologue from the musical or play you are auditioning for unless specifically told to do so by the directors:</strong>  Look for audition pieces that show you have the ability to play the type of character that you are hoping for, but from another show.  Often that means another piece by the same writer or composer.  That said, it is the style and character type that is important.
            </li>
            &nbsp;
            <li><strong>Do Not… show up in costume:</strong> It’s unprofessional.  It also will not help the director see you in that character regardless of how much you think it will.</li>
            &nbsp; 
            <li><strong>Do Not… wear clothing you cannot move freely in:</strong> You need to be able to move.  Wear comfortable, flexible clothing that will allow you to stretch and bend, dance, or run without limiting you and without ‘wardrobe malfunctions’.</li>
            &nbsp; 
            <li><strong>Do Not… wear flip flops or any kind of clunky shoe.</strong>  You cannot go on stage in bare feet. You need shoes.  Dance shoes are best, but a good athletic shoe will usually work as well.</li>
            &nbsp;
            <li><strong>Do Not… forget your headshot:</strong> It doesn’t matter if the director is your Aunt or Uncle, theatre etiquette demands you bring a headshot.  Showing up without one tells the director that you expect special treatment and that is just not something the director can afford to give anyone.</li>
            &nbsp;
            <li><strong>Do Not… fail to fill out your audition form completely and to the best of your ability:</strong>  If there is something that you don’t know, find someone who is not busy to help you. This is a matter of respect and if you show the directors disrespect on the form, they will assume you will not be reliable in the cast.  That’s just the way it works.</li>
            &nbsp;
            <li><strong>Do Not… change your look between auditions and call backs:</strong>  As a matter of fact, if at all possible, wear the same clothes you did at the audition (or at least the same color scheme and style).  We know, in many places that would be considered gross.  But… in the theatre, it is not only acceptable, it is preferred.  The reality is the director often remembers “that girl in the blue top” more than names or faces.</li>
            &nbsp;
            <li><strong>Do Not… make excuses:</strong> If you are sick, we can tell. If you have been "super busy" at school and haven't had time to practice your monologue.....well, that probably means you will continue to be super busy, and you probably won't have time to dedicate to rehearsing the show. </li>
            &nbsp;
            <li><strong>Do Not… forget to tell us if there is a role you will not accept:</strong> Enough said.</li>
            &nbsp;
            <li><strong>Do Not… forget that it is illegal to alter lines and scripts:</strong> Instead, be diligent about researching roles. If you are cast in a role that has lines or actions you don't want to or are not allowed to do or say...there isn't anything the directors can change so be aware of the demands of each role and list any you cannot accept on your audition form.</li>
            &nbsp;
            <li><strong>Do Not… be afraid to help other auditioning actors:</strong> When you assist somebody else you show that you are team player and willing to put your own ego aside.</li>
            &nbsp;
            <li><strong>Do Not… apologize:</strong> If you make a mistake or lose your lines during your monologue we probably won't know, unless you tell us by apologizing. Just improvise through, or end it with no one the wiser.</li>
            &nbsp; 
            <li><strong>Do Not… end with a look or word that indicates you thought you did badly:</strong> The last thing you want to do is put that idea into the director’s head.  Besides, even if you did, you might have stumbled onto some aspect of the character the director is looking for and ending with a look of disgust breaks that magic and could well lose you the role you just backed into.</li>
            &nbsp;
            <li><strong>Do Not… ask, should I stand or is it okay to sit?</strong> The actor’s role is to portray the character in that character’s current situation.  That means, it is up to you to decide whether you sit or stand unless the director has already instructed otherwise.</li>
            &nbsp;
            <li><strong>Do Not… be afraid to take chances when you cold read:</strong> Even if you don't do it the way we want, if you take risks we know you are willing to do the same with a character down the road.</li>
            &nbsp;
            <li><strong>Do Not… tell the directors "I can't":</strong> Especially when they give you a direction during cold reading. No matter how silly or difficult the direction seems. Often we are trying to see how well you follow directions and how willing you are to try difficult things. If you are not sure about it, ask a question, but do not say “I can’t”.  Try.</li>
          </ul>
          <p>If you avoid these traps and begin work on the elements that will <a href="tips.php">Improve Your Audition Chances in the Theatre</a> you will be ahead of the game.  Break a leg!</p>
