					the benefit of the kids. We strive to provide a safe place for the youth of east county to stretch their wings 
					in all areas of the theatre arts, reqardless of who they are or where they come from...</p>
                         <h2>Corbett Children’s Theater Origins</h2>
                         <p>For many years CCT served as the defacto theatre arts program for the Corbett School district. Starting in June of 1998 in Susan Scott’s back yard CCT worked with the local school district to provide a fun and educational experience for the students of eastern Multnomah County in the performing arts. </p>
                         <p>Corbett Children’s Theater started with a performance of Peter Pan practiced in Susan Scott’s back yard during the summer and performed in the Corbett School District’s Multi-purpose room that fall. Next was a performance of Shakespeare’s A Midsummer’s Nights Dream.</p>
                         <p>Within a couple years CCT was invited by the school district to use the Springdale School building as a storage and rehearsal space. Eventually, it became a performance space and during this time the families that make up CCT banded together to clean and restore the old school building.  Eventually a need for an earthquake retrofit of the building made that facility unavailable to CCT, shuffling us back up to use the Multi-purpose room until growth within the school district meant that they simply did not have room to accommodate Corbett Children’s Theater.</p>
                         <p>Since then we have found a home on Stark Street in Troutdale, down the hill from Mount Hood Community College, but through it all the mission has remained the same.  We exist to serve, stretch, facilitate, and educate the kids through the use of the theater arts.</p>
